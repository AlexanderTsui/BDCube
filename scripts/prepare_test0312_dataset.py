import argparse
import ast
import csv
import gzip
import json
import logging
import shutil
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable, Optional

import torch
from transformers import CLIPTokenizerFast

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from cube3d.inference.utils import load_config, load_model_weights, parse_structured
from cube3d.model.autoencoder.one_d_autoencoder import OneDAutoEncoder
from cube3d.training.data import MESH_SUFFIXES, _compute_bbox, _sample_point_cloud, load_scaled_mesh
from cube3d.training.block_diffusion import select_device

SUCCESS_STATUSES = {"downloaded", "verified_existing"}
SUPPORTED_SUFFIXES = MESH_SUFFIXES


@dataclass
class CandidateRecord:
    item_id: str
    source_key: str
    sha256: str
    file_identifier: str
    aesthetic_score: Optional[float]
    captions: list[str]
    primary_caption: str
    local_path: Path
    original_caption: str
    clean_record_path: Optional[Path]


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Build a small clean token-paired dataset under dataset/test0312."
    )
    parser.add_argument(
        "--input-root",
        type=str,
        default="dataset/minor_dataset",
        help="Root directory of the partially downloaded minor dataset.",
    )
    parser.add_argument(
        "--output-root",
        type=str,
        default="dataset/test0312",
        help="Output directory for the curated subset and paired tokens.",
    )
    parser.add_argument(
        "--config-path",
        type=str,
        default="cube3d/configs/open_model_block_diffusion.yaml",
        help="Cube model config used to load the shape tokenizer and text tokenizer name.",
    )
    parser.add_argument(
        "--shape-ckpt-path",
        type=str,
        default="model_weights/shape_tokenizer.safetensors",
        help="Path to the pretrained Cube shape tokenizer checkpoint.",
    )
    parser.add_argument(
        "--max-samples",
        type=int,
        default=0,
        help="Maximum number of valid samples to select. Use 0 or a negative value to keep all usable samples.",
    )
    parser.add_argument(
        "--point-samples",
        type=int,
        default=8192,
        help="Number of surface points sampled per mesh before shape tokenization.",
    )
    parser.add_argument(
        "--source-key",
        type=str,
        default="objaverse_xl_sketchfab",
        help="Downloader source key(s) to draw samples from. Use a comma-separated list or 'all'.",
    )
    parser.add_argument(
        "--device",
        type=str,
        default=None,
        help="Optional explicit device, e.g. cuda, cuda:0, cpu.",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=0,
        help="Random seed used for deterministic point sampling.",
    )
    parser.add_argument(
        "--overwrite-output",
        action="store_true",
        help="If set, remove the existing output directory before rebuilding it.",
    )
    parser.add_argument(
        "--clean-manifest-path",
        type=str,
        default=None,
        help="Optional clean caption manifest generated by scripts/clean_test0312_texts.py.",
    )
    parser.add_argument(
        "--require-clean-text",
        action="store_true",
        help="When set, only keep samples that exist in --clean-manifest-path.",
    )
    return parser.parse_args()


def _load_latest_progress(progress_path: Path) -> dict[str, dict]:
    latest: dict[str, dict] = {}
    with progress_path.open("r", encoding="utf-8") as handle:
        for line in handle:
            if not line.strip():
                continue
            payload = json.loads(line)
            latest[payload["item_id"]] = payload
    return latest


def _load_available_source_keys(manifest_path: Path) -> list[str]:
    source_keys: set[str] = set()
    with gzip.open(manifest_path, "rt", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            source_key = str(row.get("source_key", "")).strip()
            if source_key:
                source_keys.add(source_key)
    return sorted(source_keys)


def _parse_source_keys(input_root: Path, source_key_arg: str) -> tuple[str, ...]:
    manifest_path = input_root / "manifest.csv.gz"
    requested = [part.strip() for part in str(source_key_arg).split(",") if part.strip()]
    if not requested:
        raise ValueError("--source-key must not be empty")
    if len(requested) == 1 and requested[0].lower() in {"all", "*"}:
        return tuple(_load_available_source_keys(manifest_path))
    # Preserve user order while deduplicating.
    ordered_unique = dict.fromkeys(requested)
    return tuple(ordered_unique.keys())


def _parse_captions(raw_value: object) -> list[str]:
    if raw_value is None:
        return []
    if isinstance(raw_value, list):
        return [str(item) for item in raw_value if str(item).strip()]
    text = str(raw_value).strip()
    if not text or text in {"[]", "nan", "NaN"}:
        return []
    try:
        parsed = ast.literal_eval(text)
    except Exception:
        return [text]
    if isinstance(parsed, list):
        return [str(item).strip() for item in parsed if str(item).strip()]
    parsed_text = str(parsed).strip()
    return [parsed_text] if parsed_text else []


def _iter_candidates(
    input_root: Path,
    source_keys: tuple[str, ...],
    clean_caption_map: Optional[dict[str, dict[str, object]]] = None,
    require_clean_text: bool = False,
) -> Iterable[CandidateRecord]:
    manifest_path = input_root / "manifest.csv.gz"
    progress_path = input_root / "progress.jsonl"
    latest = _load_latest_progress(progress_path)
    source_key_set = set(source_keys)

    with gzip.open(manifest_path, "rt", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            if row["source_key"] not in source_key_set:
                continue
            progress = latest.get(row["item_id"])
            if progress is None or progress.get("status") not in SUCCESS_STATUSES:
                continue
            local_relpath = progress.get("local_path")
            if not local_relpath:
                continue
            local_path = (input_root / local_relpath).resolve()
            if not local_path.exists():
                continue
            if local_path.suffix.lower() not in SUPPORTED_SUFFIXES:
                continue
            if local_path.name.endswith(".part"):
                continue

            captions = _parse_captions(row.get("captions"))
            if not captions:
                continue

            original_caption = captions[0]
            clean_record = (
                clean_caption_map.get(str(row["item_id"]))
                if clean_caption_map is not None
                else None
            )
            if require_clean_text and clean_record is None:
                continue
            primary_caption = original_caption
            clean_record_path = None
            if clean_record is not None:
                clean_text = str(clean_record.get("text", "")).strip()
                if clean_text:
                    primary_caption = clean_text
                    clean_record_path_value = clean_record.get("clean_record_path")
                    if clean_record_path_value:
                        clean_record_path = Path(str(clean_record_path_value)).expanduser().resolve()

            aesthetic_score = row.get("aesthetic_score")
            yield CandidateRecord(
                item_id=str(row["item_id"]),
                source_key=str(row["source_key"]),
                sha256=str(row["sha256"]),
                file_identifier=str(row["file_identifier"]),
                aesthetic_score=float(aesthetic_score) if aesthetic_score else None,
                captions=captions,
                primary_caption=primary_caption,
                local_path=local_path,
                original_caption=original_caption,
                clean_record_path=clean_record_path,
            )


def _sample_slug(candidate: CandidateRecord) -> str:
    stem = candidate.local_path.stem
    return f"{stem}_{candidate.sha256[:8]}"


def _copy_mesh(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)


def _save_json(path: Path, payload: object) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False))


def _load_clean_caption_map(path: Path) -> dict[str, dict[str, object]]:
    records: dict[str, dict[str, object]] = {}
    with path.open("r", encoding="utf-8") as handle:
        for line in handle:
            if not line.strip():
                continue
            payload = json.loads(line)
            item_id = payload.get("item_id")
            if not isinstance(item_id, str) or not item_id.strip():
                raise ValueError(f"Clean manifest row is missing item_id: {payload}")
            records[item_id] = payload
    return records


def main() -> None:
    args = _parse_args()
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")

    input_root = Path(args.input_root).expanduser().resolve()
    output_root = Path(args.output_root).expanduser().resolve()
    source_keys = _parse_source_keys(input_root, args.source_key)
    clean_manifest_path = (
        Path(args.clean_manifest_path).expanduser().resolve()
        if args.clean_manifest_path is not None
        else None
    )
    clean_caption_map = (
        _load_clean_caption_map(clean_manifest_path)
        if clean_manifest_path is not None
        else None
    )
    if output_root.exists():
        if not args.overwrite_output:
            raise FileExistsError(
                f"Output root already exists: {output_root}. Pass --overwrite-output to rebuild it."
            )
        shutil.rmtree(output_root)
    output_root.mkdir(parents=True, exist_ok=False)

    torch.manual_seed(args.seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(args.seed)

    device = torch.device(args.device) if args.device is not None else select_device()
    logging.info("Using device: %s", device)
    logging.info("Using source keys: %s", ", ".join(source_keys))

    cfg = load_config(args.config_path)
    tokenizer_name = cfg.text_model_pretrained_model_name_or_path
    text_tokenizer = CLIPTokenizerFast.from_pretrained(tokenizer_name)

    shape_model = OneDAutoEncoder(
        parse_structured(OneDAutoEncoder.Config, cfg.shape_model)
    )
    load_model_weights(shape_model, args.shape_ckpt_path)
    shape_model = shape_model.eval().to(device)

    meshes_dir = output_root / "meshes"
    pairs_dir = output_root / "pairs"
    records_dir = output_root / "records"
    logs_dir = output_root / "logs"
    for path in (meshes_dir, pairs_dir, records_dir, logs_dir):
        path.mkdir(parents=True, exist_ok=True)

    manifest_records: list[dict] = []
    pair_manifest_records: list[dict] = []
    selected_records: list[dict] = []
    rejected_records: list[dict] = []
    selection_target = "all" if args.max_samples <= 0 else str(args.max_samples)

    for candidate in _iter_candidates(
        input_root,
        source_keys,
        clean_caption_map=clean_caption_map,
        require_clean_text=args.require_clean_text,
    ):
        if args.max_samples > 0 and len(selected_records) >= args.max_samples:
            break

        slug = _sample_slug(candidate)
        mesh_dst = meshes_dir / f"{slug}{candidate.local_path.suffix.lower()}"
        pair_dst = pairs_dir / f"{slug}.pt"
        record_dst = records_dir / f"{slug}.json"

        try:
            mesh = load_scaled_mesh(str(candidate.local_path))
            point_cloud = _sample_point_cloud(mesh, args.point_samples).unsqueeze(0)
            bbox_xyz = _compute_bbox(mesh)
            encoded = text_tokenizer(
                [candidate.primary_caption],
                max_length=text_tokenizer.model_max_length,
                padding="max_length",
                truncation=True,
                return_tensors="pt",
            )
            with torch.no_grad():
                shape_output = shape_model.encode(point_cloud.to(device))
            shape_ids = shape_output[3]["indices"].long().cpu()
            if shape_ids.shape[1] != shape_model.cfg.num_encoder_latents:
                raise ValueError(
                    f"Expected {shape_model.cfg.num_encoder_latents} shape tokens, got {shape_ids.shape[1]}"
                )

            _copy_mesh(candidate.local_path, mesh_dst)
            pair_payload = {
                "item_id": candidate.item_id,
                "source_key": candidate.source_key,
                "sha256": candidate.sha256,
                "mesh_path": str(mesh_dst.resolve()),
                "text": candidate.primary_caption,
                "original_text": candidate.original_caption,
                "all_captions": candidate.captions,
                "bbox_xyz": bbox_xyz.cpu(),
                "text_input_ids": encoded["input_ids"].cpu(),
                "text_attention_mask": encoded["attention_mask"].cpu(),
                "shape_ids": shape_ids,
                "num_shape_tokens": int(shape_ids.shape[1]),
                "text_seq_len": int(encoded["input_ids"].shape[1]),
                "point_samples": int(args.point_samples),
                "caption_source": "clean_manifest"
                if clean_manifest_path is not None
                else "original_manifest",
            }
            torch.save(pair_payload, pair_dst)

            record_payload = {
                "item_id": candidate.item_id,
                "source_key": candidate.source_key,
                "sha256": candidate.sha256,
                "file_identifier": candidate.file_identifier,
                "source_mesh_path": str(candidate.local_path),
                "mesh_path": str(mesh_dst.resolve()),
                "pair_path": str(pair_dst.resolve()),
                "text": candidate.primary_caption,
                "original_text": candidate.original_caption,
                "all_captions": candidate.captions,
                "bbox_xyz": [float(v) for v in bbox_xyz.tolist()],
                "text_token_count": int(encoded["attention_mask"].sum().item()),
                "text_seq_len": int(encoded["input_ids"].shape[1]),
                "shape_token_count": int(shape_ids.shape[1]),
                "aesthetic_score": candidate.aesthetic_score,
                "clean_record_path": str(candidate.clean_record_path)
                if candidate.clean_record_path is not None
                else None,
                "caption_source": "clean_manifest"
                if clean_manifest_path is not None
                else "original_manifest",
            }
            _save_json(record_dst, record_payload)

            manifest_record = {
                "item_id": candidate.item_id,
                "mesh_path": str(mesh_dst.relative_to(output_root)),
                "text": candidate.primary_caption,
                "original_text": candidate.original_caption,
                "pair_path": str(pair_dst.relative_to(output_root)),
                "record_path": str(record_dst.relative_to(output_root)),
            }
            pair_manifest_record = {
                "item_id": candidate.item_id,
                "pair_path": str(pair_dst.relative_to(output_root)),
                "mesh_path": str(mesh_dst.relative_to(output_root)),
                "text": candidate.primary_caption,
                "original_text": candidate.original_caption,
                "bbox_xyz": [float(v) for v in bbox_xyz.tolist()],
                "shape_token_count": int(shape_ids.shape[1]),
                "text_seq_len": int(encoded["input_ids"].shape[1]),
            }
            manifest_records.append(manifest_record)
            pair_manifest_records.append(pair_manifest_record)
            selected_records.append(record_payload)
            logging.info(
                "Selected %d/%s: %s (%s)",
                len(selected_records),
                selection_target,
                candidate.item_id,
                candidate.local_path.name,
            )
        except Exception as error:
            rejected = {
                "item_id": candidate.item_id,
                "source_key": candidate.source_key,
                "mesh_path": str(candidate.local_path),
                "text": candidate.primary_caption,
                "error": repr(error),
            }
            rejected_records.append(rejected)
            logging.warning("Rejected %s: %s", candidate.item_id, error)
            continue

    if args.max_samples > 0 and len(selected_records) < args.max_samples:
        raise RuntimeError(
            f"Only built {len(selected_records)} valid samples, fewer than requested {args.max_samples}."
        )

    manifest_path = output_root / "manifest.jsonl"
    pair_manifest_path = output_root / "pair_manifest.jsonl"
    with manifest_path.open("w", encoding="utf-8") as handle:
        for record in manifest_records:
            handle.write(json.dumps(record, ensure_ascii=False) + "\n")
    with pair_manifest_path.open("w", encoding="utf-8") as handle:
        for record in pair_manifest_records:
            handle.write(json.dumps(record, ensure_ascii=False) + "\n")

    _save_json(
        output_root / "summary.json",
        {
            "input_root": str(input_root),
            "output_root": str(output_root),
            "source_key": args.source_key,
            "source_keys": list(source_keys),
            "max_samples": int(args.max_samples),
            "point_samples": int(args.point_samples),
            "device": str(device),
            "config_path": str(Path(args.config_path).expanduser().resolve()),
            "shape_ckpt_path": str(Path(args.shape_ckpt_path).expanduser().resolve()),
            "clean_manifest_path": str(clean_manifest_path) if clean_manifest_path else None,
            "require_clean_text": bool(args.require_clean_text),
            "text_tokenizer": tokenizer_name,
            "shape_num_encoder_latents": int(shape_model.cfg.num_encoder_latents),
            "shape_num_codes": int(shape_model.cfg.num_codes),
            "manifest_path": str(manifest_path),
            "pair_manifest_path": str(pair_manifest_path),
            "selected_count": len(selected_records),
            "selected_samples": selected_records,
            "rejected_count": len(rejected_records),
        },
    )
    _save_json(logs_dir / "rejected_samples.json", rejected_records)
    logging.info("Prepared %d samples under %s", len(selected_records), output_root)


if __name__ == "__main__":
    main()
