from __future__ import annotations

import argparse
import json
import logging
import os
import shutil
import sys
from pathlib import Path
from typing import Any

import torch
from transformers import CLIPTokenizerFast

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from cube3d.inference.utils import load_config


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Materialize a cleaned token-pair dataset by reusing raw shape tokens "
            "and rewriting only the text fields from a clean manifest."
        )
    )
    parser.add_argument(
        "--raw-root",
        type=str,
        default="dataset/test0313_all_raw",
        help="Root directory containing raw records/pairs/meshes.",
    )
    parser.add_argument(
        "--clean-manifest-path",
        type=str,
        default="dataset/test0313_text_clean/clean_manifest.jsonl",
        help="Clean caption manifest generated by scripts/clean_test0312_texts.py.",
    )
    parser.add_argument(
        "--output-root",
        type=str,
        default="dataset/test0312",
        help="Destination directory for the cleaned final dataset.",
    )
    parser.add_argument(
        "--config-path",
        type=str,
        default="cube3d/configs/open_model_block_diffusion.yaml",
        help="Cube config used to resolve the text tokenizer.",
    )
    parser.add_argument(
        "--overwrite-output",
        action="store_true",
        help="Delete the output directory before rebuilding it.",
    )
    parser.add_argument(
        "--mesh-link-mode",
        type=str,
        choices=("hardlink", "symlink", "copy"),
        default="hardlink",
        help="How to materialize meshes into the output dataset.",
    )
    return parser.parse_args()


def _load_clean_manifest(path: Path) -> dict[str, dict[str, Any]]:
    records: dict[str, dict[str, Any]] = {}
    with path.open("r", encoding="utf-8") as handle:
        for line in handle:
            if not line.strip():
                continue
            payload = json.loads(line)
            item_id = str(payload.get("item_id", "")).strip()
            if not item_id:
                raise ValueError(f"Clean manifest row is missing item_id: {payload}")
            records[item_id] = payload
    return records


def _ensure_parent(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)


def _write_json(path: Path, payload: Any) -> None:
    _ensure_parent(path)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False))


def _write_jsonl(path: Path, rows: list[dict[str, Any]]) -> None:
    _ensure_parent(path)
    with path.open("w", encoding="utf-8") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=False) + "\n")


def _materialize_mesh(src: Path, dst: Path, mode: str) -> None:
    _ensure_parent(dst)
    if dst.exists() or dst.is_symlink():
        dst.unlink()
    if mode == "hardlink":
        try:
            os.link(src, dst)
            return
        except OSError:
            logging.warning("Hardlink failed for %s, falling back to copy", src)
            shutil.copy2(src, dst)
            return
    if mode == "symlink":
        dst.symlink_to(src)
        return
    shutil.copy2(src, dst)


def main() -> None:
    args = _parse_args()
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")

    raw_root = Path(args.raw_root).expanduser().resolve()
    clean_manifest_path = Path(args.clean_manifest_path).expanduser().resolve()
    output_root = Path(args.output_root).expanduser().resolve()

    records_dir = raw_root / "records"
    if not records_dir.exists():
        raise FileNotFoundError(f"Raw records directory not found: {records_dir}")
    if not clean_manifest_path.exists():
        raise FileNotFoundError(f"Clean manifest not found: {clean_manifest_path}")

    if output_root.exists():
        if not args.overwrite_output:
            raise FileExistsError(
                f"Output root already exists: {output_root}. Pass --overwrite-output to rebuild it."
            )
        shutil.rmtree(output_root)

    (output_root / "records").mkdir(parents=True, exist_ok=True)
    (output_root / "pairs").mkdir(parents=True, exist_ok=True)
    (output_root / "meshes").mkdir(parents=True, exist_ok=True)
    (output_root / "logs").mkdir(parents=True, exist_ok=True)

    clean_map = _load_clean_manifest(clean_manifest_path)
    cfg = load_config(args.config_path)
    tokenizer = CLIPTokenizerFast.from_pretrained(cfg.text_model_pretrained_model_name_or_path)

    manifest_rows: list[dict[str, Any]] = []
    pair_manifest_rows: list[dict[str, Any]] = []
    selected_records: list[dict[str, Any]] = []
    failures: list[dict[str, Any]] = []

    record_paths = sorted(records_dir.glob("*.json"))
    for index, record_path in enumerate(record_paths, start=1):
        try:
            raw_record = json.loads(record_path.read_text())
            item_id = str(raw_record["item_id"])
            clean_row = clean_map.get(item_id)
            if clean_row is None:
                raise KeyError(f"Missing clean caption for item_id={item_id}")

            clean_text = str(clean_row.get("text", "")).strip()
            if not clean_text:
                raise ValueError(f"Empty clean text for item_id={item_id}")

            raw_pair_path = Path(str(raw_record["pair_path"])).expanduser().resolve()
            raw_mesh_path = Path(str(raw_record["mesh_path"])).expanduser().resolve()
            if not raw_pair_path.exists():
                raise FileNotFoundError(f"Raw pair not found: {raw_pair_path}")
            if not raw_mesh_path.exists():
                raise FileNotFoundError(f"Raw mesh not found: {raw_mesh_path}")

            slug = record_path.stem
            mesh_dst = output_root / "meshes" / raw_mesh_path.name
            pair_dst = output_root / "pairs" / f"{slug}.pt"
            record_dst = output_root / "records" / f"{slug}.json"

            encoded = tokenizer(
                [clean_text],
                max_length=tokenizer.model_max_length,
                padding="max_length",
                truncation=True,
                return_tensors="pt",
            )

            raw_pair = torch.load(raw_pair_path, map_location="cpu")
            raw_pair["text"] = clean_text
            raw_pair["original_text"] = raw_record.get("original_text", raw_pair.get("original_text"))
            raw_pair["text_input_ids"] = encoded["input_ids"].cpu()
            raw_pair["text_attention_mask"] = encoded["attention_mask"].cpu()
            raw_pair["text_seq_len"] = int(encoded["input_ids"].shape[1])
            raw_pair["caption_source"] = "clean_manifest"
            raw_pair["clean_record_path"] = str(
                Path(str(clean_row.get("clean_record_path"))).expanduser().resolve()
            )

            _materialize_mesh(raw_mesh_path, mesh_dst, args.mesh_link_mode)
            torch.save(raw_pair, pair_dst)

            bbox_xyz = raw_pair["bbox_xyz"]
            if isinstance(bbox_xyz, torch.Tensor):
                bbox_values = [float(value) for value in bbox_xyz.view(-1).tolist()]
            else:
                bbox_values = [float(value) for value in bbox_xyz]

            record_payload = {
                "item_id": item_id,
                "source_key": raw_record.get("source_key"),
                "sha256": raw_record.get("sha256"),
                "file_identifier": raw_record.get("file_identifier"),
                "source_mesh_path": raw_record.get("source_mesh_path", str(raw_mesh_path)),
                "mesh_path": str(mesh_dst.resolve()),
                "pair_path": str(pair_dst.resolve()),
                "text": clean_text,
                "original_text": raw_record.get("original_text"),
                "all_captions": raw_record.get("all_captions"),
                "bbox_xyz": bbox_values,
                "text_token_count": int(encoded["attention_mask"].sum().item()),
                "text_seq_len": int(encoded["input_ids"].shape[1]),
                "shape_token_count": int(raw_pair["shape_ids"].shape[-1]),
                "aesthetic_score": raw_record.get("aesthetic_score"),
                "clean_record_path": raw_pair["clean_record_path"],
                "source_record_path": str(record_path.resolve()),
                "source_pair_path": str(raw_pair_path),
                "caption_source": "clean_manifest",
            }
            _write_json(record_dst, record_payload)

            manifest_rows.append(
                {
                    "item_id": item_id,
                    "mesh_path": str(mesh_dst.relative_to(output_root)),
                    "text": clean_text,
                    "original_text": raw_record.get("original_text"),
                    "pair_path": str(pair_dst.relative_to(output_root)),
                    "record_path": str(record_dst.relative_to(output_root)),
                }
            )
            pair_manifest_rows.append(
                {
                    "item_id": item_id,
                    "pair_path": str(pair_dst.relative_to(output_root)),
                    "mesh_path": str(mesh_dst.relative_to(output_root)),
                    "text": clean_text,
                    "original_text": raw_record.get("original_text"),
                    "bbox_xyz": bbox_values,
                    "shape_token_count": int(raw_pair["shape_ids"].shape[-1]),
                    "text_seq_len": int(encoded["input_ids"].shape[1]),
                }
            )
            selected_records.append(record_payload)

            if index % 100 == 0:
                logging.info("Materialized %d/%d cleaned samples", index, len(record_paths))
        except Exception as error:
            failure = {
                "record_path": str(record_path.resolve()),
                "error": repr(error),
            }
            failures.append(failure)
            logging.warning("Failed to materialize %s: %s", record_path.name, error)

    _write_jsonl(output_root / "manifest.jsonl", manifest_rows)
    _write_jsonl(output_root / "pair_manifest.jsonl", pair_manifest_rows)
    _write_json(output_root / "logs" / "failures.json", failures)
    _write_json(
        output_root / "summary.json",
        {
            "raw_root": str(raw_root),
            "clean_manifest_path": str(clean_manifest_path),
            "output_root": str(output_root),
            "config_path": str(Path(args.config_path).expanduser().resolve()),
            "mesh_link_mode": args.mesh_link_mode,
            "requested_records": len(record_paths),
            "clean_manifest_count": len(clean_map),
            "materialized_count": len(selected_records),
            "failure_count": len(failures),
        },
    )
    logging.info(
        "Finished materializing cleaned dataset: %d samples, %d failures",
        len(selected_records),
        len(failures),
    )


if __name__ == "__main__":
    main()
